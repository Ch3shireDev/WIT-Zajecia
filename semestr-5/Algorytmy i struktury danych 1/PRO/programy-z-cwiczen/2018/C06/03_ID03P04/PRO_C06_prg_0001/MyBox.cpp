///*****************************************************************************************************
MyBox::MyBox(MyA x2, MyA x1, MyA x0){
    this->x2 = x2;
    this->x1 = x1;
    this->x0 = x0;

    SetCrC();
    }
///*****************************************************************************************************
int MyBox::CrC(){
    return 64*x2.GetCrC() + 8*x1.GetCrC() + x0.GetCrC();
    }
///*****************************************************************************************************
int MyBox::SetCrC(){
    cRc = CrC();
    return cRc;
    }
///*****************************************************************************************************
///*****************************************************************************************************
ostream& operator<<(ostream& s, MyBox myBox){
    s<<"["<<myBox.x2<<", "<<myBox.x1<<", "<<myBox.x0<<"] -> "<<myBox.cRc;
    return s;
    }
///*****************************************************************************************************
istream& operator>>(istream& s, MyBox& myBox){
    s>>myBox.x2>>myBox.x1>>myBox.x0;
    myBox.SetCrC();
    return s;
    }
