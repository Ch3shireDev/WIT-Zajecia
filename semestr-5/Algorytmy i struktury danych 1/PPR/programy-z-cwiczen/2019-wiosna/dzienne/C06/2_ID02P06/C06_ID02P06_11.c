#include <stdio.h>
///************************************************************************
int MyRead(char*);
int Abs(int);
int Max2(int, int);
int Max3(int, int, int);
int Max7(int, int, int, int, int, int, int);
///************************************************************************
///************************************************************************
int MyRead(char* myStr){
    int x;
    printf("%s",myStr);
    scanf("%d",&x);
    return x;
    }
///************************************************************************
int Abs(int x){
    if(x<0)return -x;
    return x;
    }
///************************************************************************
int Max2(int x0, int x1){
    if(x0>x1) return x0;
    return x1;
    }
///************************************************************************
int Max3(int x0, int x1, int x2){
    return Max2(Max2(x0,x1),x2);
    }
///************************************************************************
int Max7(int x0, int x1, int x2, int x3, int x4, int x5, int x6){
    return Max3(Max3(x0, x1, x2), Max3(x3, x4, x5), x6);
    }
///************************************************************************
///************************************************************************
int main(){
    int x0, x1, x2, x3, x4, x5, x6;
    x0 = MyRead("x0? = ");
    x1 = MyRead("x1? = ");
    x2 = MyRead("x2? = ");
    x3 = MyRead("x3? = ");
    x4 = MyRead("x4? = ");
    x5 = MyRead("x5? = ");
    x6 = MyRead("x6? = ");

    printf("Max(%d, %d, %d)= %d\n",x0, x1, x2, Max3(x0, x1, x2));
    printf("Max(%d, %d, %d, %d, %d, %d, %d)= %d\n",x0, x1, x2, x3, x4, x5, x6, Max7(x0, x1, x2, x3, x4, x5, x6));

    return 0;
    }













