/************************************************************************/
void MySwap(int* a, int* b){
    int tmp;

    tmp=*a;
    *a=*b;
    *b=tmp;
    }
/************************************************************************/
int* MyTabIntX01(int* sT){
    int r;
    int *mT;
    r=MyRead();
    *sT=r;
    mT=(int*)malloc(sizeof(int)*r);

    return mT;
    }
