#include <stdio.h>
/******************************************************************/
int MyRead();
int MyAbs(int);
/******************************************************************/
/******************************************************************/
int MyRead(){
    int x;

    printf("x ?=");
    scanf("%d", &x);

    return x;
    }
/******************************************************************/
int MyAbs(int x){
    if(x<0) return -x;
    return x;
    }
/******************************************************************/
/******************************************************************/
int main(){
    int y, myAbs;

    y = MyRead();

    myAbs = MyAbs(y);


    printf("|%d| = %d\n", y, myAbs);

    return 0;
    }
