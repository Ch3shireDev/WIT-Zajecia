#include <stdio.h>
/***************************************************************************************/
/***************************************************************************************/
struct MyStruct01{
    int x;
    int y;
    };
/***************************************************************************************/
/***************************************************************************************/

/***************************************************************************************/
/***************************************************************************************/
int main(){
    struct MyStruct01 myS01;
    int * wp;

    myS01.x = 7;
    myS01.y = 12;

    printf("mS01.x = %d\n",myS01.x);
    printf("mS01.y = %d\n",myS01.y);

    wp=(int*)&myS01;
    printf("mS01.x = %d\n",*wp++);
    printf("mS01.y = %d\n",*wp);


    return 0;
    }
