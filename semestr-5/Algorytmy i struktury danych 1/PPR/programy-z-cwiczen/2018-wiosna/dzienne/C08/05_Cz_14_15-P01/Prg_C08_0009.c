#include<stdio.h>
#include<stdlib.h>
/************************************************************************/
/************************************************************************/
int MyRead();
int MyEx(int, int, int(*mF)(int, int));
int MyMax(int, int);
int MyMin(int, int);
/************************************************************************/
/************************************************************************/
int MyRead(){
    int x;
    printf("x ?=");
    scanf("%d", &x);

    return x;
    }
/** 3b, 4b, 5b */
/************************************************************************/
int MyEx(int x, int y, int(*mF)(int, int)){
    return mF(x,y);
    }
/************************************************************************/
int MyMax(int x, int y){
    if(x>y) return x;
    return y;
    }
/************************************************************************/
int MyMin(int x, int y){
    if(x<y) return x;
    return y;
    }
/************************************************************************/
/************************************************************************/
int main(){
    int x, y;
    x = MyRead();
    y = MyRead();

    printf("Max(%d, %d) = %d\n", x, y, MyEx(x, y, MyMax));
    printf("Min(%d, %d) = %d\n", x, y, MyEx(x, y, MyMin));
    return 0;
    }
