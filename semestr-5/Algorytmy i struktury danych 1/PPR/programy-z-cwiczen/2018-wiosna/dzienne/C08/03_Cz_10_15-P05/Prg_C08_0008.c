#include <stdio.h>
#include <stdlib.h>
/***************************************************************************************************/
/***************************************************************************************************/
typedef int (*ptrMyComp)(int, int);
/***************************************************************************************************/
/***************************************************************************************************/
int MyTest(int x, int y, int (*mmF)(int, int));
int MyCalc(int, int, ptrMyComp);
int MySubs(int, int);
int MyAdd(int, int);
int MyMulti(int, int);
int MyFun(int, int);
/***************************************************************************************************/
/***************************************************************************************************/
int MyTest(int x, int y, int(*mmF)(int, int)){
    return mmF(x,y);
    }
/***************************************************************************************************/
int MyCalc(int x, int y, ptrMyComp mF){
    return mF(x,y);
    }
/***************************************************************************************************/
int MySubs(int x, int y){
    return x-y;
    }
/***************************************************************************************************/
int MyAdd(int x, int y){
    return x+y;
    }
/***************************************************************************************************/
int MyMulti(int x, int y){
    return x*y;
    }
/***************************************************************************************************/
int MyFun(int x, int y){
    return 2*x - 3*y;
    }
/***************************************************************************************************/
/***************************************************************************************************/
int main(){
    int x, y;
    x=9;
    y=4;
    printf("sum = %d\n", MyCalc(x,y,MyAdd));
    printf("sub = %d\n", MyCalc(x,y,MySubs));
    printf("mul = %d\n", MyCalc(x,y,MyMulti));
    printf("fun = %d\n", MyCalc(x,y,MyFun));
    printf("fun = %d\n", MyTest(x,y,MyFun));
    return 0;
    }
