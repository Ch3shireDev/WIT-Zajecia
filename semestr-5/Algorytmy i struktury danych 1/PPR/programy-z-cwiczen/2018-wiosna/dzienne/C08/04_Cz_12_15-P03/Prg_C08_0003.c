#include<stdio.h>
/******************************************************************************/
/******************************************************************************/
typedef void (*ptrMyText)();
/******************************************************************************/
/******************************************************************************/
void MyText();
void MyText1();
void MyText2();
/******************************************************************************/
/******************************************************************************/
void MyText(){
    printf("\n\n************* MyText ***********\n");
    printf("******************    ");
    MyText1();
    printf("******************    ");
    MyText2();
    }
/******************************************************************************/
void MyText1(){
    printf("---------> MyText1\n");
    }
/******************************************************************************/
void MyText2(){
    printf(">>>>>>>>>> MyText2\n");
    }
/******************************************************************************/
/******************************************************************************/
int main(){
    ptrMyText myF;

    myF=MyText1;
    myF();

    myF=MyText2;
    myF();

    myF=MyText;
    myF();

    return 0;
    }
