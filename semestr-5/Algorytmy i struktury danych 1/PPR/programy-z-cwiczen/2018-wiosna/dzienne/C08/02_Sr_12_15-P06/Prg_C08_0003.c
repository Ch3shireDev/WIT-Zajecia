#include<stdio.h>
#include<stdlib.h>
/*********************************************************************/
/*********************************************************************/
typedef int (*ptrMyCom)(int, int);
/*********************************************************************/
/*********************************************************************/
int MyRead();
int MyAdd(int, int);
int MyMul(int, int);
/*********************************************************************/
/*********************************************************************/
int MyRead(){
    int x;
    printf("x ?=");
    scanf("%d", &x);

    return x;
    }
/*********************************************************************/
int MyAdd(int x, int y){
    return x+y;
    }
/*********************************************************************/
int MyMul(int x, int y){
    return x*y;
    }
/*********************************************************************/
/*********************************************************************/
int main(){
    ptrMyCom myF;
    int x, y;

    x= MyRead();
    y= MyRead();

    myF=MyAdd;
    printf("%d + %d = %d\n",x,y,myF(x,y));

    myF=MyMul;
    printf("%d * %d = %d\n",x,y,myF(x,y));

    return 0;
    }










