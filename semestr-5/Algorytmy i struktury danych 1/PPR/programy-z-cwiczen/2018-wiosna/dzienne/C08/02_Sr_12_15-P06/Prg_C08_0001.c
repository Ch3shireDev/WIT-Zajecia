void MyText01(){
    printf("********> MyText01\n");
    }
/*********************************************************************/
void MyText02(){
    printf("########> MyText02\n");
    }
/*********************************************************************/
void MyTextAll(){
    printf("**************> MyTextAll <**************\n");
    MyText01();
    MyText02();
    printf("*****************************************\n");
    }
