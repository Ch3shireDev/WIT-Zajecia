/*********************************************************************/
int main(){
    MyText01();
    printf("MyText01 -----> %d\n", (int)MyText01);
    MyText02();
    printf("MyText02 -----> %d\n", (int)MyText02);
    MyTextAll();
    printf("MyTextAll ----> %d\n", (int)MyTextAll);

    return 0;
    }
