#include<stdio.h>
/************************************************************************/
/************************************************************************/
typedef void (*ptrMyText)();
typedef int (*ptrMyComp)(int, int);
/************************************************************************/
/************************************************************************/
int MyRead();
void MyText();
void MyText1();
void MyText2();
int MyAdd(int, int);
int MyMul(int, int);
/************************************************************************/
/************************************************************************/
int MyRead(){
    int x;
    printf("x ?=");
    scanf("%d", &x);

    return x;
    }
/************************************************************************/
void MyText(){
    printf("*********** MyText ************\n");
    printf("--------------> ");
    MyText1();
    printf("--------------> ");
    MyText2();
    }
/************************************************************************/
void MyText1(){
    printf(">>>>>>>>>>>>>> MyText1\n");
    }
/************************************************************************/
void MyText2(){
    printf("//////////////////////// MyText2\n");
    }
/************************************************************************/
int MyAdd(int x, int y){
    return x + y;
    }
/************************************************************************/
int MyMul(int x, int y){
    return x * y;
    }
/************************************************************************/
/************************************************************************/
int main(){
    ptrMyComp mT;
    int x, y;
    x = MyRead();
    y = MyRead();

    mT = MyAdd;
    printf("%d + %d = %d\n",x, y, mT(x, y));

    mT = MyMul;
    printf("%d * %d = %d\n",x, y, mT(x, y));

    return 0;
    }
