#include<stdio.h>
/************************************************************************/
/************************************************************************/
typedef void (*ptrMyText)();
/************************************************************************/
/************************************************************************/
void MyText();
void MyText1();
void MyText2();
/************************************************************************/
/************************************************************************/
void MyText(){
    printf("*********** MyText ************\n");
    printf("--------------> ");
    MyText1();
    printf("--------------> ");
    MyText2();
    }
/************************************************************************/
void MyText1(){
    printf(">>>>>>>>>>>>>> MyText1\n");
    }
/************************************************************************/
void MyText2(){
    printf("//////////////////////// MyText2\n");
    }
/************************************************************************/
/************************************************************************/
int main(){
    ptrMyText myF;

    myF = MyText1;
    myF();
    printf("myF  ---> %d\tMyText1 ---> %d\n",myF, MyText1);
    printf("\n\n");

    myF = MyText2;
    myF();
    printf("myF  ---> %d\tMyText2 ---> %d\n",myF, MyText2);
    printf("\n\n");

    myF = MyText;
    myF();
    printf("myF  ---> %d\tMyText  ---> %d\n",myF, MyText);
    printf("\n\n");


    return 0;
    }
