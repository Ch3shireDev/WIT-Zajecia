#include <stdio.h>
#include <stdlib.h>
/**************************************************************************************/
typedef struct MyL{
    int x;
    struct MyL* next;
    }MyList;
/**************************************************************************************/
/**************************************************************************************/

/**************************************************************************************/
/**************************************************************************************/


/**************************************************************************************/
/**************************************************************************************/
int main(){
    MyList mL00;
    MyList mL01;
    MyList mL02;
    MyList mL03;
    MyList mL04;

    mL00.x = 0;
    mL00.next = &mL01;
    mL00.next->x = 1;
    mL01.next = &mL02;
    mL01.next->x = 2;
    mL02.next = &mL03;
    mL02.next->x = 3;
    mL03.next = &mL04;
    mL03.next->x = 4;
    mL04.next = NULL;

    printf("mL00.x = %d\n", mL00.x);
    printf("mL01.x = %d\n", mL00.next->x);
    printf("mL02.x = %d\n", mL00.next->next->x);
    printf("mL04.x = %d\n", mL00.next->next->next->x);
    printf("mL05.x = %d\n", mL01.next->next->next->x);

    return 0;
    }
