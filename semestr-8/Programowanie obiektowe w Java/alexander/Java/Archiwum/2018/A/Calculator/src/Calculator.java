   private void DoCalculate(String nextOperation)
   {
      double CurrValue;
      if(NeedReset&&(nextOperation==null)) CurrValue=OldValue;
      else CurrValue=Double.parseDouble(EDisplay.getText());
      if((Operation==null)&&(nextOperation==null)) Operation=OldOperation;
      if(map.containsKey(Operation))
      {
         OldValue=CurrValue;
         CurrValue=map.get(Operation).exec(SaveValue, CurrValue);
         EDisplay.setText(String.format("%s",CurrValue).replace(",","."));
      }
      NeedReset=true;
      SaveValue=CurrValue;
      Operation=nextOperation;
      if(nextOperation!=null) OldOperation=Operation;
   }
   
