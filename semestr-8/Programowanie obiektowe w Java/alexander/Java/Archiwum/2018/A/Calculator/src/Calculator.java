   private static final HashMap<String,OperationClass> makemap()
   {
      final HashMap<String,OperationClass> tmp=new HashMap<>();
      tmp.put("+", (double a,double b) -> { return a+b; } );
      tmp.put("-", (double a,double b) -> { return a-b; } );
      tmp.put("*", (double a,double b) -> { return a*b; } );
      tmp.put("/", new OperationClass() { @Override public double exec(double a,double b) { return a/b; } } );
      return tmp;
   }
   
