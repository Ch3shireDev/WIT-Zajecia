class RowForList
{
   private String name;
   private int value;

   public RowForList(String str,int value)
   {
      this.name=str;
      this.value=value;
   }

   public String getStr()
   {
      return name;
   }

   public void setStr(String str)
   {
      this.name=str;
   }

   public int getValue()
   {
      return value;
   }

   public void setValue(int value)
   {
      this.value=value;
   }

   @Override public String toString()
   {
      return String.format("%s: %d",name,value);
   }
}