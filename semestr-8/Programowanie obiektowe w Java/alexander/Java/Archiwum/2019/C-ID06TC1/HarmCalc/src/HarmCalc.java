   private double calc(long count)
   {
      double sum=0;
      for(long i=1;i<=count;++i) sum+=1D/i;
      return sum;
   }

   private void execute(long count)
   {
      EdResult.append(String.format("%d -> %.13f",count,calc(count))+System.lineSeparator());
   }

   private void BtnCalcActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_BtnCalcActionPerformed
   {//GEN-HEADEREND:event_BtnCalcActionPerformed
      EdResult.selectAll();
      EdResult.requestFocus();
      new Thread(() -> execute(count)).start();
   }//GEN-LAST:event_BtnCalcActionPerformed

   private void ThreadRun()
   {
      for(int i=0;i<=10;++i)
      {
         EdResult.append(String.format("%d%%",10*i)+System.lineSeparator());
         try { Thread.sleep(1000); }
         catch(Exception ex) {}
      }
   }

