   private void BtnAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAddItemActionPerformed
      int index=CommandList.getSelectedIndex();
      Command cmd=model.elementAt(index);
      content.addElement(cmd);
      CommandList.requestFocus();
   }//GEN-LAST:event_BtnAddItemActionPerformed

   private void BtnMakeCommandActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnMakeCommandActionPerformed
      String name=TreningName.getText().trim();
      Command[] list=new Command[content.size()];
      for(int i=0;i<content.size();++i) list[i]=content.get(i);
      model.addElement(new Trening(name,list));
      CommandList.setSelectedIndex(model.size()-1);
      content.clear();
      CommandList.requestFocus();
   }//GEN-LAST:event_BtnMakeCommandActionPerformed

   private void BtnRemoveCommandActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRemoveCommandActionPerformed
      int index=CommandList.getSelectedIndex();
      if(index>=3) model.remove(index);
      CommandList.requestFocus();
   }//GEN-LAST:event_BtnRemoveCommandActionPerformed

   private void BtnExecuteCommandActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnExecuteCommandActionPerformed
      int index=CommandList.getSelectedIndex();
      Command cmd=model.elementAt(index);
      Log.append(cmd.execute(new Sportsman())+"\n");
      CommandList.requestFocus();
   }//GEN-LAST:event_BtnExecuteCommandActionPerformed

   private void CommandListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_CommandListMouseClicked
      if(evt.getClickCount()==2)
      {
         int index=CommandList.getSelectedIndex();
         Command tr=model.elementAt(index);
         TreningName.setText(tr.name());
         content.clear();
         if(tr instanceof Trening)
         {
            for(Command cmd:((Trening)tr).getList()) content.addElement(cmd);
         }
         TreningName.requestFocus();
      }
   }//GEN-LAST:event_CommandListMouseClicked

